#Comparision operators in python are >,<,<=,>=,==,!=
#let's see all these operators with some variables
x=3
y=2
z=2
print("Greater than = ",x>y)
print("Lesser than = ",x<y)
print("Greater than and equal to = ",x>=z)
print("Lesser than and equal to  = ",x<=z)
print("Equal to = ",y==z)
print("Not equal to = ",x!=y)
